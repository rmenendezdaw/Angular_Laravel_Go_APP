func UsersRegistration(c *gin.Context) {
	userModelValidator := NewUserModelValidator()
	if err := userModelValidator.Bind(c); err != nil {
		c.JSON(http.StatusUnprocessableEntity, common.NewValidatorError(err))
		return
	}
	// err := SaveOne(&userModelValidator.userModel)
	if err := SaveOne(&userModelValidator.userModel); err != nil {
		fmt.Println("hola");
		if (strings.Contains(err.Error(), "uix_user_models_email")) {
			c.JSON(http.StatusNotFound, common.NewError("Database:", errors.New("Email alredy in use")))
			return			
		}
		c.JSON(http.StatusUnprocessableEntity, common.NewError("database", err))
		return
	}
	c.Set("my_user_model", userModelValidator.userModel)
	serializer := UserSerializer{c}

	common.SetUserRedis(userModelValidator.User.Email, serializer.Response().Token)
	c.JSON(http.StatusCreated, gin.H{"user": serializer.Response()})
}

func UsersLogin(c *gin.Context) {
	loginValidator := NewLoginValidator()
	if err := loginValidator.Bind(c); err != nil {
		c.JSON(http.StatusUnprocessableEntity, common.NewValidatorError(err))
		return
	}
	userModel, err := FindOneUser(&UserModel{Email: loginValidator.userModel.Email})

	if err != nil {
		c.JSON(http.StatusForbidden, common.NewError("login", errors.New("Not Registered email or invalid password")))
		return
	}

	if userModel.checkPassword(loginValidator.User.Password) != nil {
		c.JSON(http.StatusForbidden, common.NewError("login", errors.New("Not Registered email or invalid password")))
		return
	}
	UpdateContextUserModel(c, userModel.ID)
	serializer := UserSerializer{c}

	common.SetUserRedis(loginValidator.User.Email, serializer.Response().Token)

	c.JSON(http.StatusOK, gin.H{"user": serializer.Response()})
}

